	<script type="text/javascript">
		$(document).ready(function() {
			$('input').on("keypress", function(e) {
				if (e.keyCode == 13) {
					if($('#login').css('display') == 'inline-block') {
						$('.login').click();
						return true;
					}

					if($('#register').css('display') == 'inline-block') {
						$('.register').click();
						return true;
					}
					return false;
				}
			});

			$('.login').on('click', function() {
				$('.login').html('<i class="fas fa-atom fa-spin"></i>');
				var formdata = $("#login_form").serialize();
				$.ajax({
					url: 'login.php',
					type: 'POST',
					data: formdata,
					dataType: "json",
					success: function (result) {
						if(result['error'] === true) {
							$('.login').html('Login');
							// We supply a title in our error messages but PHPAuth doesn't
							// so, if `result['title']` doesn't exist, just print 'Error'
							toastr.error(result['message'], result['title'] || 'Error');
						} else if(result['2fa'] === true) {
							$('.login').html('Submit code');
							// If we find out that the user has 2FA enabled, display the field
							// for them to enter the code
							toastr.success('A code has been sent to (xxx) xxx-xx' + result['phone'], 'Code sent!');
							$("#code").inputmask({"mask": "9999", showMaskOnHover: false});
							$('.code').show();
							$('.code').focus();
						} else {
							window.location.href = "home";
						}
					}
				});
			});

			$('.register').on('click', function() {
				if($('#register_email').val().length < 1|| $('#register_password').val().length < 1) {
					toastr.error('Please fill out the register form', 'Error');
					return
				}
				$('.register').html('<i class="fas fa-atom fa-spin"></i>');
				var formdata = $("#register_form").serialize();
				$.ajax({
					url: 'register.php',
					type: 'POST',
					data: formdata,
					dataType: "json",
					success: function (result) {
						if(result['error'] === true) {
							$('.register').html('Register');
							toastr.error(result['message'], result['title'] || 'Error');
						} else {
							$('.register').html('Logging in!');
							toastr.success(result['message'], result['title'] || 'Success',  { timeOut: 1500, onHidden: function() { window.location.href = "home.php"; }});
						}
						console.log(result);
					}
				});
			});
		});
	</script>
